#include "memory.h"
#include <cstring>
#include <vector>
#include <array>
#include <cstdint>
#include <vector>

const std::array<uint8_t, 80> Memory::fontset = {
    0xF0, 0x90, 0x90, 0x90, 0xF0, // 0
    0x20, 0x60, 0x20, 0x20, 0x70, // 1
    0xF0, 0x10, 0xF0, 0x80, 0xF0, // 2
    0xF0, 0x10, 0xF0, 0x10, 0xF0, // 3
    0x90, 0x90, 0xF0, 0x10, 0x10, // 4
    0xF0, 0x80, 0xF0, 0x10, 0xF0, // 5
    0xF0, 0x80, 0xF0, 0x90, 0xF0, // 6
    0xF0, 0x10, 0x20, 0x40, 0x40, // 7
    0xF0, 0x90, 0xF0, 0x90, 0xF0, // 8
    0xF0, 0x90, 0xF0, 0x10, 0xF0, // 9
    0xF0, 0x90, 0xF0, 0x90, 0x90, // A
    0xE0, 0x90, 0xE0, 0x90, 0xE0, // B
    0xF0, 0x80, 0x80, 0x80, 0xF0, // C
    0xE0, 0x90, 0x90, 0x90, 0xE0, // D
    0xF0, 0x80, 0xF0, 0x80, 0xF0, // E
    0xF0, 0x80, 0xF0, 0x80, 0x80  // F
};

Memory::Memory() { ram.fill(0); }

uint8_t Memory::read(uint16_t addr) const {
    if (addr < ram.size()) return ram[addr];
    return 0;
}

void Memory::write(uint16_t addr, uint8_t value) {
    if (addr < ram.size()) ram[addr] = value;
}

void Memory::loadFont() {
    for (size_t i = 0; i < fontset.size(); i++){
        ram[i] = fontset[i];
    }
}

void Memory::loadROM(const std::vector<uint8_t>& rom, uint16_t offset) {
    for (size_t i = 0; i < rom.size(); i++) {
        if (offset + i < ram.size()) {
            ram[offset + i] = rom[i];
        } else {
            break;
        }
    }
}
